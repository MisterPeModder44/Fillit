#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	if (!s1)
		return (!s2 ? 1 : 0);
	else if (!s2)
		return (!s1 ? 1 : 0);
	return (ft_strcmp(s1, s2) == 0 ? 1 : 0);
}
